// 1. Declare a global variable age
let age = 20;

// 2. Function that logs the value of age
function displayAge() {
    console.log("Age is:", age);
}

// 3. Function that changes the value of age
function changeAge() {
    age = 30;         
    console.log("Updated age is:", age);
}

// 4. Call the functions
displayAge();   
changeAge();   
displayAge();   
